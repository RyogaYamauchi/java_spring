public class Operators{
  public static void main(String[] args){
    int value1=4;
    int value2=8;
    System.out.printf("%d + %d = %d\n",value1,value2,value1+value2);
    System.out.printf("%d - %d = %d\n",value1,value2,value1-value2);
    System.out.printf("%d * %d = %d\n",value1,value2,value1*value2);
    System.out.printf("%d / %d = %d\n",value1,value2,value1/value2);
    System.out.printf("%d %% %d = %d\n",value1,value2,value1%value2);
  }
}
